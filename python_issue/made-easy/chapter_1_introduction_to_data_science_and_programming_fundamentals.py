"""Introduction to Data Science and Programming Fundamentals."""

# # Введение в Data Science
#
#
# **Data Science** - это научная дисциплина, которая включает в себя извлечение информации
# из огромных объемов данных с использованием различных научных
# методов, алгоритмов и процессов. Эта наука помогает обнаруживать в необработанных
# данных скрытые закономерности.
#
#
# Эта наука помогает нам:
# - задавать правильные вопросы;
# - находить основную причину проблемы;
# - находить закономерности среди, на первый взгляд, хаотичных необработанных
# данных;
# - создавать модели для предиктивного анализа;
# - визуализировать и отображать результаты с помощью графиков, информацион-
# ных панелей и т. д.;
# - наделять машины интеллектом;
# - определять лояльность клиентов с помощью анализа настроений;
# - принимать более качественные и быстрые решения;
# - рекомендовать правильный продукт нужному клиенту для развития нашего бизнеса.
#
#
# **Машинное обучение** - это инструмент для извлечения знаний из данных.
# В машинном обучении модели могут обучаться на данных самостоятельно или
# поэтапно: обучение с учителем, т. е. на данных, подготовленных человеком, или
# обучение без учителя, в котором работа ведется над хаотичными и неорганизованными
# данными.
#
# **Глубокое обучение** - это создание многослойных нейронных сетей в областях,
# где требуется более продвинутый или быстрый анализ, а традиционное машинное
# обучение не справляется. Под глубиной понимается наличие более одного
# скрытого слоя нейронов в сети, которые проводят математические вычисления.
#
# **Большие данные** - это работа с огромными объемами часто неструктурированных
# данных. Специфика этой сферы - инструменты и системы, способ1;1ые
# выдерживать высокие нагрузки.
#
#
# **Статистика**
# Статистика лежит в основе Data Science. Правильная обработка статистики
# может помочь вам извлечь больше информации и получить более значимые
# результаты.
#
# **Базы данных**
# Будучи специалистом по данным, вам необходимо понимать, как работают базы
# данных, как ими управлять и как извлекать из них данные.
#
# **Моделирование**
# Математические модели позволяют выполнять вычисления и прогнозы на основе
# того, что вы уже знаете о данных. Моделирование также относится к машинному
# обучению и включает определение того, какой алгоритм больше подходит
# для решения данной проблемы и как обучать эти модели.
#
#
# **Программирование** - это идеи, преобразованные в пошаговые
# инструкции, понятные компьютеру. Такая пошаговая инструкция называется **алгоритмом**.
#
# **Алгоритм** - это конечная последовательность четко определенных,
# реализуемых компьютером инструкций для решения какой-то проблемы или
# для выполнения вычислений. Алгоритмы всегда однозначны и используются для
# вычислений, обработки данных, автоматизированных решений и других задач.
#
#
# ### Реальный пример алгоритма
#
#
# Шаг 1: Начало.
# Шаг 2: Возьмите ковшик и налейте в него один стакан воды.
# Шаг 3: Зажгите конфорку и поставьте на нее ковшик.
# Шаг 4: Доведите воду до кипения.
# Шаг 5: Добавьте в кипящую воду две столовые ложки сахара и две столовые ложки чайных листьев.
# Шаг 6: Добавьте в кипящую смесь одну чашку молока.
# Шаг 7: Варите содержимое в течение одной минуты.
# Шаг 8: Выключите конфорку.
# Шаг 9: Процедите чай через сито.
# Шаг 10: Разлейте чай по чашкам и подавайте.
# Шаг 11: Конец.
#
#
# Алгоритмы можно изображать в графической форме с использованием определенных
# обозначений. Полученное изображение называется блок-схемой.
# В блок-схемах для представления различных функций программ используются
# некоторые стандартные символы.
#
#
# Компилятор - это программа, преобразующая исходный код в машинный.
#
# Интерпретатор - это компьютерная программа,
# которая непосредственно выполняет инструкции, написанные на языке
# программирования, без предварительной компиляции в программу на машинном
# языке.

