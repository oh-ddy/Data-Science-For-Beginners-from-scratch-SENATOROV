# поддерживаемостью, тестируемостью, эффективностью, безопасностью, удобством использования,
# расширяемостью, переносимостью и соблюдением принятых стандартов кодирования.
#
# 4. Какой язык понимает компьютер?
#
# Машинный код
#
# 5. Чем языки программирования отличаются от языков, на которых мы говорим?
#
# Языки программирования строго формализованы, однозначны и предназначены для общения с машиной,
# тогда как естественные языки гибкие, многозначные и служат для общения между людьми.
#
#
# 1.5.2. Правда или ложь
#
# 1. Машинное обучение - это инструмент для извлечения знаний из данных.
# Правда
#
# 2. Глубокое обучение - это то же самое, что машинное обучение.
# Ложь, глубокое обучение — подмножество машинного обучения
#
# 3. Все инженеры-программисты также могут считаться специалистами по данным.
# Ложь
#
# 4. Статистика - важный инструмент для специалистов по данным.
# Правда
#
# 5. Компьютер может принимать решения, выходящие за рамки данных ему инструкций,
# подстраиваясь под изменения среды.
# Ложь, строго по заданной инструкции
#
# 6. Компьютеры понимают языки программирования «как есть».
# Ложь, компьютер «понимает» только машинный код; языки программирования преобразуются
# в него с помощью компиляторов/интерпретаторов
#
# 7. Некоторые языки программирования компилируются, некоторые интерпретируются,
# а некоторые используют и то и другое.
# Правда
#
# 8. Все программы выполняются последовательно.
# Правда
#
# 9. В IDЕ есть встроенный текстовый редактор.
# Правда
#
# 10. Компиляторы и интерпретаторы - это такие механизмы, наподобие привода
# для компакт-дисков.
# Ложь
